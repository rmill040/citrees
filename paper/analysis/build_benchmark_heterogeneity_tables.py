"""Build dataset-heterogeneity tables for the paper rewrite.

This script asks whether aggregate benchmark claims are broad across datasets
or concentrated in a few slices. It mirrors classification and regression under
the same paper-facing benchmark contract used by the cleaned summary layer.

Outputs:
  - paper/results/tables/paper_heterogeneity_dataset_method_surface.csv
  - paper/results/tables/paper_heterogeneity_method_summary.csv
  - paper/results/tables/paper_heterogeneity_cif_dataset_profile.csv
  - paper/results/tables/paper_heterogeneity_cif_pairwise_by_dataset.csv
  - paper/results/tables/paper_heterogeneity_cif_pairwise_breadth.csv

The strict CIF-vs-baseline pairwise paper surface is
`paper_heterogeneity_cif_pairwise_breadth.csv`. The older
`paper_heterogeneity_cif_pairwise_summary.csv` artifact is superseded and is
not regenerated by this builder.

Usage:
  uv run python paper/analysis/build_benchmark_heterogeneity_tables.py
"""

from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd

from paper.analysis.benchmark_common import (
    STANDARD_K,
    TABLES_DIR,
    TASK_CONFIG,
    complete_case_scores,
    dataset_scores,
    load_real_task_frame,
    rank_complete_case_scores,
    select_best_task_configs,
)


def _build_dataset_method_surface(ranked: pd.DataFrame, task: str, metric: str) -> pd.DataFrame:
    """Aggregate each method within dataset over all supported downstream and k cells."""
    surface = (
        ranked.groupby(["dataset", "method_base", "method_id"], as_index=False)
        .agg(
            n_cells=("rank", "size"),
            mean_rank=("rank", "mean"),
            median_rank=("rank", "median"),
            best_rank=("rank", "min"),
            worst_rank=("rank", "max"),
            mean_score=("dataset_mean_score", "mean"),
            median_score=("dataset_mean_score", "median"),
        )
        .sort_values(["dataset", "mean_rank", "method_base"])
        .reset_index(drop=True)
    )

    surface["n_methods"] = surface.groupby("dataset")["method_base"].transform("nunique")
    surface["dataset_rank_position"] = surface.groupby("dataset")["mean_rank"].rank(
        ascending=True,
        method="average",
    )
    surface["top_half_cutoff"] = (surface["n_methods"] + 1) // 2
    surface["is_top_half"] = surface["dataset_rank_position"] <= surface["top_half_cutoff"]
    surface.insert(0, "task", task)
    surface.insert(1, "metric", metric)
    surface["support_type"] = "dataset_mean_over_all_complete_case_downstream_k"
    return surface


def _build_method_summary(surface: pd.DataFrame) -> pd.DataFrame:
    """Summarize how broadly each method performs across datasets."""
    summary = (
        surface.groupby(["task", "metric", "method_base", "method_id"], as_index=False)
        .agg(
            n_datasets=("dataset", "nunique"),
            mean_dataset_cells=("n_cells", "mean"),
            mean_rank=("mean_rank", "mean"),
            median_rank=("mean_rank", "median"),
            best_dataset_position=("dataset_rank_position", "min"),
            worst_dataset_position=("dataset_rank_position", "max"),
            top1_share=("dataset_rank_position", lambda x: float((x == 1).mean())),
            top3_share=("dataset_rank_position", lambda x: float((x <= 3).mean())),
            top_half_share=("is_top_half", "mean"),
            mean_score=("mean_score", "mean"),
        )
        .sort_values(["task", "mean_rank", "method_base"])
        .reset_index(drop=True)
    )
    summary["rank_position"] = summary.groupby("task")["mean_rank"].rank(
        ascending=True, method="average"
    )
    return summary


def _build_cif_dataset_profile(surface: pd.DataFrame, focus_method: str) -> pd.DataFrame:
    """Profile where the focus method wins, contends, or trails by dataset."""
    leaders = (
        surface.sort_values(
            ["task", "dataset", "dataset_rank_position", "mean_rank", "method_base"]
        )
        .groupby(["task", "dataset"], as_index=False)
        .first()
        .rename(
            columns={
                "method_base": "leader_method",
                "method_id": "leader_method_id",
                "mean_rank": "leader_mean_rank",
                "mean_score": "leader_mean_score",
                "dataset_rank_position": "leader_rank_position",
            }
        )
    )

    focus = surface[surface["method_base"] == focus_method].copy()
    profile = focus.merge(
        leaders[
            [
                "task",
                "dataset",
                "leader_method",
                "leader_method_id",
                "leader_mean_rank",
                "leader_mean_score",
                "leader_rank_position",
            ]
        ],
        on=["task", "dataset"],
        how="left",
    )
    profile["rank_gap_to_leader"] = profile["mean_rank"] - profile["leader_mean_rank"]
    profile["score_gap_to_leader"] = profile["mean_score"] - profile["leader_mean_score"]
    return profile.sort_values(
        ["task", "dataset_rank_position", "mean_rank", "dataset"]
    ).reset_index(drop=True)


def _build_pairwise_by_dataset(scores: pd.DataFrame, task: str, focus_method: str) -> pd.DataFrame:
    """Aggregate focus-method differences against each compared method by dataset."""
    rows: list[dict[str, object]] = []
    focus = scores[scores["method_base"] == focus_method][
        ["dataset", "downstream_model", "k", "dataset_mean_score"]
    ]
    focus = focus.rename(columns={"dataset_mean_score": "focus_score"})

    for baseline in sorted(m for m in scores["method_base"].unique() if m != focus_method):
        base = scores[scores["method_base"] == baseline][
            ["dataset", "downstream_model", "k", "dataset_mean_score"]
        ]
        base = base.rename(columns={"dataset_mean_score": "baseline_score"})
        merged = focus.merge(base, on=["dataset", "downstream_model", "k"], how="inner")
        if merged.empty:
            continue

        merged["delta"] = merged["focus_score"] - merged["baseline_score"]

        by_dataset = (
            merged.groupby("dataset", as_index=False)
            .agg(
                n_cells=("delta", "size"),
                mean_delta=("delta", "mean"),
                median_delta=("delta", "median"),
                min_delta=("delta", "min"),
                max_delta=("delta", "max"),
                win_share=("delta", lambda x: float((x > 0).mean())),
                nonloss_share=("delta", lambda x: float((x >= 0).mean())),
            )
            .sort_values(["mean_delta", "dataset"], ascending=[False, True])
            .reset_index(drop=True)
        )
        by_dataset.insert(0, "task", task)
        by_dataset.insert(1, "focus_method", focus_method)
        by_dataset.insert(2, "baseline", baseline)
        rows.extend(by_dataset.to_dict(orient="records"))

    return pd.DataFrame(rows)


def _build_pairwise_breadth(pairwise_by_dataset: pd.DataFrame) -> pd.DataFrame:
    """Summarize how broad each focus-vs-baseline result is across datasets."""
    summary = (
        pairwise_by_dataset.groupby(["task", "focus_method", "baseline"], as_index=False)
        .agg(
            n_datasets=("dataset", "nunique"),
            mean_dataset_cells=("n_cells", "mean"),
            mean_delta=("mean_delta", "mean"),
            q25_delta=("mean_delta", lambda x: float(x.quantile(0.25))),
            median_delta=("mean_delta", "median"),
            q75_delta=("mean_delta", lambda x: float(x.quantile(0.75))),
            wins=("mean_delta", lambda x: int((x > 0).sum())),
            losses=("mean_delta", lambda x: int((x < 0).sum())),
            ties=("mean_delta", lambda x: int((x == 0).sum())),
            win_share=("mean_delta", lambda x: float((x > 0).mean())),
            nonloss_share=("mean_delta", lambda x: float((x >= 0).mean())),
            mean_cell_win_share=("win_share", "mean"),
            mean_cell_nonloss_share=("nonloss_share", "mean"),
        )
        .sort_values(["task", "mean_delta", "baseline"], ascending=[True, False, True])
        .reset_index(drop=True)
    )
    summary["support_type"] = "dataset_mean_over_all_complete_case_downstream_k"
    return summary


def main() -> None:
    """Build and save dataset-heterogeneity tables for the paper benchmark."""
    parser = argparse.ArgumentParser(
        description="Build dataset heterogeneity tables for the paper benchmark"
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=TABLES_DIR,
        help="Directory for heterogeneity summary outputs",
    )
    args = parser.parse_args()

    output_dir = args.output_dir.resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    dataset_surfaces: list[pd.DataFrame] = []
    pairwise_frames: list[pd.DataFrame] = []

    for task, config in TASK_CONFIG.items():
        raw = load_real_task_frame(task=task)
        raw = raw[raw["k"].isin(STANDARD_K)].copy()
        metric = config["metric"]
        focus_method = config["focus_method"]

        _, best_configs = select_best_task_configs(raw, metric)
        best_full = raw.merge(
            best_configs[["method_base", "method_id"]], on=["method_base", "method_id"], how="inner"
        )
        scores = dataset_scores(best_full, metric)
        complete = complete_case_scores(scores)
        ranked = rank_complete_case_scores(complete)

        dataset_surfaces.append(_build_dataset_method_surface(ranked, task, metric))
        pairwise_frames.append(_build_pairwise_by_dataset(complete, task, focus_method))

    dataset_surface = pd.concat(dataset_surfaces, ignore_index=True)
    pairwise_by_dataset = pd.concat(pairwise_frames, ignore_index=True)

    outputs = {
        "paper_heterogeneity_dataset_method_surface.csv": dataset_surface,
        "paper_heterogeneity_method_summary.csv": _build_method_summary(dataset_surface),
        "paper_heterogeneity_cif_dataset_profile.csv": _build_cif_dataset_profile(
            dataset_surface, focus_method="cif"
        ),
        "paper_heterogeneity_cif_pairwise_by_dataset.csv": pairwise_by_dataset,
        "paper_heterogeneity_cif_pairwise_breadth.csv": _build_pairwise_breadth(
            pairwise_by_dataset
        ),
    }

    for filename, frame in outputs.items():
        out_path = output_dir / filename
        frame.to_csv(out_path, index=False)
        print(f"Saved {out_path}")

    superseded_path = output_dir / "paper_heterogeneity_cif_pairwise_summary.csv"
    if superseded_path.exists():
        print(
            "Superseded artifact retained but not regenerated: "
            f"{superseded_path} -> use paper_heterogeneity_cif_pairwise_breadth.csv"
        )


if __name__ == "__main__":
    main()
